# Define special character variables

# Null character
$NullChar = [char]0

# Alert character
$AlertChar = "`a"

# Backspace character
$BackspaceChar = "`b"

# Escape character (added in PowerShell 6)
$EscapeChar = "`e"

# Form feed character
$FormFeedChar = "`f"

# New line character
$NewLineChar = "`n"

# Carriage return character
$CarriageReturnChar = "`r"

# Horizontal tab character
$TabChar = "`t"

# Vertical tab character
$VerticalTabChar = "`v"
